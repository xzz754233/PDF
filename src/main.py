import os
import sys
import markdown
from xhtml2pdf import pisa
from crewai import Agent, Task, Crew, Process
from crewai.tools import BaseTool
from crewai_tools import SerperDevTool
from pydantic import BaseModel, Field
from typing import Type
from dotenv import load_dotenv

load_dotenv()

class PDFGeneratorInput(BaseModel):
    output_filename: str = Field(..., description="The final PDF filename.")

class PDFGeneratorTool(BaseTool):
    name: str = "Generate PDF Report"
    description: str = "Compiles generated files into a PDF report."
    args_schema: Type[BaseModel] = PDFGeneratorInput

    def _run(self, output_filename: str) -> str:
        try:
            os.makedirs(os.path.dirname(output_filename), exist_ok=True)
            
            # 定義 CSS：確保 Code Block (pre) 會換行且好看
            css_style = """
            <style>
                @page { size: A4; margin: 2cm; }
                body { font-family: Helvetica, sans-serif; font-size: 10pt; line-height: 1.6; color: #333; }
                h1 { border-bottom: 2px solid #2c3e50; padding-bottom: 10px; margin-top: 30px; }
                h2 { color: #e67e22; margin-top: 25px; border-bottom: 1px solid #eee; }
                
                /* 讓 xhtml2pdf 能夠正確渲染程式碼 */
                pre {
                    font-family: Courier;
                    font-size: 9pt;
                    background-color: #f5f7f9;
                    border: 1px solid #ddd;
                    padding: 10px;
                    white-space: pre-wrap; /* 關鍵：保留換行 */
                    word-wrap: break-word; /* 關鍵：長行折行 */
                }
            </style>
            """
            
            # 初始化 Markdown 內容
            full_content_md = "# Project Final Report\n\nGenerated by SaaS Launchpad Crew\n\n"
            
            # 1. 讀取並合併 Phase 1 (Spec) & Phase 2 (Tech Stack)
            # 這些檔案是 Markdown 格式，包含了你的「分析部分」
            for filename in ["lite_output/1_spec.md", "lite_output/2_tech_stack.md"]:
                if os.path.exists(filename):
                    with open(filename, "r", encoding="utf-8") as f:
                        full_content_md += f"\n\n---\n\n{f.read()}"

            # 2. 讀取 Phase 3 (Raw Code) 並手動格式化
            code_path = "lite_output/3_mvp_skeleton.md"
            if os.path.exists(code_path):
                with open(code_path, "r", encoding="utf-8") as f:
                    raw_code = f.read().strip()
                
                # 我們幫 Agent 加上 Markdown Code Block 標記
                # 這樣能確保 100% 的格式正確率
                code_section_md = f"\n\n---\n\n# Phase 3: MVP Skeleton Code\n\n```python\n{raw_code}\n```"
                full_content_md += code_section_md

            # 3. 統一轉為 PDF
            html_content = markdown.markdown(full_content_md, extensions=['fenced_code', 'codehilite'])
            full_html = f"<html><head>{css_style}</head><body>{html_content}</body></html>"

            with open(output_filename, "wb") as pdf_file:
                pisa_status = pisa.CreatePDF(full_html, dest=pdf_file)

            if pisa_status.err:
                return f"Error creating PDF: {pisa_status.err}"
            
            # 4. 清理中間檔案
            for f in ["lite_output/1_spec.md", "lite_output/2_tech_stack.md", "lite_output/3_mvp_skeleton.md"]:
                if os.path.exists(f): os.remove(f)

            return f"Successfully compiled PDF report at: {output_filename}"
            
        except Exception as e:
            return f"Exception during PDF creation: {str(e)}"

# --- Main Crew Logic ---

def run_lite_crew(inputs=None):
    print("=== Initializing SaaS Launchpad Crew (Lite Version) ===")
    
    search_tool = SerperDevTool()
    pdf_tool = PDFGeneratorTool()

    # Agents
    analyst = Agent(
        role='Lead Product Analyst',
        goal='Analyze the user idea and produce a clear PRD.',
        backstory="Expert at translating vague ideas into logical docs.",
        tools=[search_tool],
        verbose=False,
        memory=True
    )

    resource_hunter = Agent(
        role='Tech Resource Scout',
        goal='Find suitable Python libraries and APIs.',
        backstory="Familiar with Python ecosystem.",
        tools=[search_tool],
        verbose=False,
        memory=True
    )

    architect = Agent(
        role='Senior Technical Architect',
        goal='Produce the MVP code structure.',
        backstory="Excels at building functional MVPs.",
        tools=[pdf_tool], 
        verbose=False,
        memory=True
    )

    # Tasks
    # Task 1: 輸出分析報告 (Markdown)
    task_analysis = Task(
        description=("Analyze the user's idea: '{task}'. Define features, risks, and competitors."),
        expected_output="A Markdown document containing the analysis.",
        agent=analyst,
        output_file="lite_output/1_spec.md" 
    )

    # Task 2: 輸出技術棧 (Markdown)
    task_resources = Task(
        description=("Recommend tech stack based on the spec."),
        expected_output="A Markdown list of tools and APIs.",
        agent=resource_hunter,
        output_file="lite_output/2_tech_stack.md"
    )

    # Task 3: 輸出純程式碼 (Raw Text) -> 這裡我們要求純文字
    task_coding = Task(
        description=(
            "Write the core 'main.py' skeleton code.\n"
            "IMPORTANT: Output ONLY the raw Python code content.\n"
            "Do NOT use markdown backticks (```).\n"
            "Do NOT write 'Here is the code'.\n"
            "Just start with 'import ...' and end with the code."
        ),
        expected_output="Raw Python code text.",
        agent=architect,
        output_file="lite_output/3_mvp_skeleton.md"
    )

    # Task 4: 觸發 PDF 生成
    task_report = Task(
        description=(
            "The previous tasks have already saved the files.\n"
            "Call the 'Generate PDF Report' tool.\n"
            "Set output_filename to 'lite_output/final_report.pdf'.\n"
            "In your final answer, just say 'DONE'."
        ),
        expected_output="The word 'DONE'.",
        agent=architect
    )

    crew = Crew(
        agents=[analyst, resource_hunter, architect],
        tasks=[task_analysis, task_resources, task_coding, task_report],
        process=Process.sequential,
        verbose=False
    )

    return crew

if __name__ == "__main__":
    print("## Local Testing Mode ##")
    user_idea = input("\nPlease enter your project idea: ")
    if not user_idea: sys.exit()
    my_crew = run_lite_crew()
    result = my_crew.kickoff(inputs={"task": user_idea})
    print("\n[Done] Check the lite_output folder.")